package com.sahaj.io;

public class Strike {

	String name;
	String strikestate;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStrikestate() {
		return strikestate;
	}
	public void setStrikestate(String strikestate) {
		this.strikestate = strikestate;
	}
}
